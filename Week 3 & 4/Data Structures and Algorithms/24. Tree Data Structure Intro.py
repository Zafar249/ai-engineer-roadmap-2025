# A tree is a non linear data structure where nodes are organized in a hierarchy
# Nodes represent a piece of data
# Edges represent relationship between nodes
# The size of a tree is the number of nodes in that tree
# A root node is a node with only outgoing edges.
# A leaf node is a node with only incoming edges.
# The depth of a tree is the number of edges below a root node
# The height of a tree is the number of edges above a leaf node