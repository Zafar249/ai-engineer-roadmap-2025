# Time complexity is the performance of an algorithm as the amount of data grows.
# O(1) is constant time complexity. 
# The algorithm will always take the same time no matter the size of the dataset. 
# O(log n) is logarithmic time complexity.
# The algorithm will take decreasingly more time as the size of the dataset grows.
# O(n) is linear time complexity.
# The algorithm will take more time as the size of the dataset grows.
# O(n^2) is quadratic time complexity.
# The algorithm will take exponentially more time as the size of the dataset grows.