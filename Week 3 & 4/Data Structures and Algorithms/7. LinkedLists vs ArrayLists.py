# In general Arrays are faster at accessing elements then linked lists.
# Arrays use indexes which they use to randomly access elements.
# Linkedlist uses pointers to navigate through the list.
# Time Complexity for Searching : Array has O(1), Linkedlist has O(n)
# In general Linked lists are faster at inserting or deleting elements.
# In Arrays each element has to be shifted foward when inserting or backwards when deleting.
# In Linked List, pointers are rearranged to add an element or remove an element from the list.
# Time Complexity for Inserting, Deleting: Array has O(n), Linkedlist has O(1).